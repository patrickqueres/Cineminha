    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        if (LstPessoas.getSelectedValue() != null) {

            Pessoa a = (Pessoa) LstPessoas.getSelectedValue();
            
            try {
                ctrlPessoa.excluir(a.getId());
                
               
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, ex);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Selecione um item para remo√ß√£o!");
        }
        
        try {
            LstPessoas.setListData(ctrlPessoa.atualizarLista().toArray());
        } catch (SQLException ex) {
            Logger.getLogger(guiAtor.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        txtEmail.setText("");
        txtNome.setText("");
        txtTelefone.setText("");
        txtNome.requestFocus();
        txtMatricula.setText("");
                
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void LstPessoasValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_LstPessoasValueChanged
        // TODO add your handling code here:
        Pessoa pessoa = new Pessoa();
        if (LstPessoas.getSelectedValue() == null) {
            return;
        }
        
        pessoa = (Pessoa) LstPessoas.getSelectedValue();
        txtEmail.setText(pessoa.getEmail());
        txtNome.setText(pessoa.getNome());
        txtTelefone.setText(pessoa.getTelefone());
        txtMatricula.setText(Long.toString(pessoa.getId()));
         


    }//GEN-LAST:event_LstPessoasValueChanged

    private void btnAtualiarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualiarActionPerformed
        // TODO add your handling code here:
       if (LstPessoas.getSelectedValue() != null) {
            Pessoa a = (Pessoa) LstPessoas.getSelectedValue();
            a.setNome(txtNome.getText());
            a.setEmail(txtEmail.getText());
            a.setTelefone(txtTelefone.getText());
            
            try {
                ctrlPessoa.alterar(a);
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, ex);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Selecione um item para atualizar!");
        }
        
        try {
            LstPessoas.setListData(ctrlPessoa.atualizarLista().toArray());
        } catch (SQLException ex) {
            Logger.getLogger(guiAtor.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        txtNome.setText("");
        txtEmail.setText("");
        txtTelefone.setText("");
        txtMatricula.setText("");
        txtNome.requestFocus();
        
        
    }//GEN-LAST:event_btnAtualiarActionPerformed

