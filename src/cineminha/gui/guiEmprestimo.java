    private void btnGravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGravarActionPerformed
        // TODO add your handling code here:
        Emprestimo f = new Emprestimo();
        f.setPessoa((Pessoa) cboPessoa.getSelectedItem());
        f.setCopia((Copia) cboCopia.getSelectedItem());
        f.setDataEmprestimo(txtDataE.getText());
        f.setDataDevolucao(txtDataD.getText());
        
        if(!chkDevolvido.isSelected()) {
            f.setDevolvido(0);
        } else {
            f.setDevolvido(1);
        }
                
        try {
            ctrlEmprestimo.cadastrar(f);
            LstEmprestimo.setListData(ctrlEmprestimo.atualizarLista().toArray());
            
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
        
        txtDataE.setText("");
        txtDataE.requestFocus();
        txtDataD.setText("");
        txtID.setText("");
        chkDevolvido.setSelected(false);
        cboCopia.setSelectedItem(null);
        cboPessoa.setSelectedItem(null);
        

    }//GEN-LAST:event_btnGravarActionPerformed

