    private void formWindowActivated(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowActivated
        // TODO add your handling code here:
        cboCopia.removeAllItems();
        cboPessoa.removeAllItems();
        try {
            // TODO add your handling code here:
            LstEmprestimo.setListData(ctrlEmprestimo.atualizarLista().toArray());
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }

        try {
            for(Object cop : ctrlCopia.atualizarLista()){ 
                cboCopia.addItem(cop);
            }
        } catch (Exception ex) {
            Logger.getLogger(guiFilme.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        try {
            for(Object pes : ctrlPessoa.atualizarLista()){ 
                cboPessoa.addItem(pes);
            }
        } catch (Exception ex) {
            Logger.getLogger(guiFilme.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_formWindowActivated

