    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        if (LstEmprestimo.getSelectedValue() != null) {
            Emprestimo a = (Emprestimo) LstEmprestimo.getSelectedValue();
            try {
                ctrlEmprestimo.excluir(a.getId());
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, ex);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Selecione um item para remoção!");
        }
        
        txtDataE.setText("");
        txtDataE.requestFocus();
        txtDataD.setText("");
        txtID.setText("");
        chkDevolvido.setSelected(false);
        cboCopia.setSelectedItem(null);
        cboPessoa.setSelectedItem(null);
        
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void LstEmprestimoValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_LstEmprestimoValueChanged
        // TODO add your handling code here:
        Emprestimo emprestimo = new Emprestimo();
        txtDataE.setText("");
        txtDataE.requestFocus();
        txtDataD.setText("");
        txtID.setText("");
        chkDevolvido.setSelected(false);
        cboCopia.setSelectedItem(null);
        cboPessoa.setSelectedItem(null);
        
        if (LstEmprestimo.getSelectedValue() == null) {
            return;
        }
        
        emprestimo = (Emprestimo) LstEmprestimo.getSelectedValue();
        txtID.setText(Long.toString(emprestimo.getId()));
        txtDataE.setText(emprestimo.getDataEmprestimo());
        txtDataD.setText(emprestimo.getDataDevolucao());
        cboCopia.setSelectedItem(emprestimo.getCopia());
        cboPessoa.setSelectedItem(emprestimo.getPessoa());
                
       if(!emprestimo.getDevolvido().equals(1) ) {
            chkDevolvido.setSelected(false);
        } 
        else {
            chkDevolvido.setSelected(true);
        }
    }//GEN-LAST:event_LstEmprestimoValueChanged

    private void btnAtualiarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualiarActionPerformed
        // TODO add your handling code here:
        Emprestimo emprestimo = new Emprestimo();
        emprestimo.setPessoa((Pessoa) cboPessoa.getSelectedItem());
        emprestimo.setCopia((Copia) cboCopia.getSelectedItem());
        emprestimo.setDataDevolucao(txtDataD.getText());
        emprestimo.setDataEmprestimo(txtDataE.getText());
        
        if(!chkDevolvido.isSelected()) {
            emprestimo.setDevolvido(0);
        } else {
            emprestimo.setDevolvido(1);
        }
        
        JOptionPane.showMessageDialog(null, "Registro atualizado!");
        
              
        try {
            ctrlEmprestimo.alterar(emprestimo);
            LstEmprestimo.setListData(ctrlEmprestimo.atualizarLista().toArray());
            
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }    
        
    }//GEN-LAST:event_btnAtualiarActionPerformed

